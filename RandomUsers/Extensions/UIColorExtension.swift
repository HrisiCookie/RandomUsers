import Foundation
import UIKit

extension UIColor {
    func maleBackground() -> UIColor {
        return UIColor(red: 145.0/255.0, green: 202.0/255.0, blue: 255.0/255.0, alpha: 1)
    }
    
    func femaleBackground() -> UIColor {
        return UIColor(red: 242.0/255.0, green: 152.0/255.0, blue: 189.0/255.0, alpha: 1)
    }
}
